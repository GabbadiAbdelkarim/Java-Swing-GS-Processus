    public void remplir()
{
    jComboBox1.removeAllItems();
    jComboBox1.addItem("");
    jComboBox2.removeAllItems();
    jComboBox2.addItem("");
    String[] Pids = controller.getProcessusids();
    String[] Eids = controller.getEployeids();
    
    for(int i = 0; i<Pids.length; i++)
        {
            jComboBox1.addItem(""+Pids[i]);    
        }
    for(int i = 0; i<Eids.length; i++)
        {
            jComboBox2.addItem(""+Eids[i]);    
        }
    jComboBox1.setSelectedItem(controller.getIdProcessus(id));
    jComboBox2.setSelectedItem(controller.getIdEmploye(id));
    //jTextArea1.setText(controller.getRapport(id));
}
